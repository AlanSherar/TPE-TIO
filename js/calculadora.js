"use strict"
class Calculadora{
     Restar(numero1 , numero2){
    return (numero1 - numero2) ;
    }
    sumar(numero1 , numero2){
        return (numero1 + numero2) ;
        }
    multiplicar(numero1 , numero2){
        return (numero1 * numero2) ;
        }
    dividir(numero1 , numero2){
        return (numero1 / numero2) ;
        }
        

}
